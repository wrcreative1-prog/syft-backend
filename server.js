// 芒聰聙芒聰聙 Run schema migration then start 芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙芒聰聙
(async () => {
  const fs   = require('fs');
  const path = require('path');
  const pool = require('./db/pool');

  // Split schema into individual statements and run each one separately
  // so a single failure doesn't prevent the rest from running.
  // Strip comment lines first so that CREATE TABLE statements preceded by
  // block comments (-- 芒聲聬芒聲聬 ...) aren't accidentally filtered out.
  const raw = fs.readFileSync(path.join(__dirname, 'db', 'schema.sql'), 'utf8');
  const sql = raw
    .split('\n')
    .filter(line => !line.trim().startsWith('--'))
    .join('\n');
  const statements = sql
    .split(';')
    .map(s => s.trim())
    .filter(s => s.length > 0);

  let ok = 0, warn = 0;
  for (const stmt of statements) {
    try {
      await pool.query(stmt);
      ok++;
    } catch (err) {
      // "already exists" errors are expected on re-deploy 芒聙聰 log but continue
      if (err.code === '42P07' || err.code === '42710' || err.message.includes('already exists')) {
        warn++;
      } else {
        console.error('芒職聽茂赂聫  Migration warning:', err.message);
        warn++;
      }
    }
  }
  console.log(`芒聹聟  Database schema ready. (${ok} ok, ${warn} skipped)`);

  app.listen(PORT, () => {
    console.log(`冒聼職聙  Syft API running on port ${PORT}`);
    console.log(`    Environment: ${process.env.NODE_ENV || 'development'}`);
  });
})();
